// Простой логгер с цветами для консоли
const colors = {
    reset: '\x1b[0m',
    red: '\x1b[31m',
    green: '\x1b[32m',
    yellow: '\x1b[33m',
    blue: '\x1b[34m',
    magenta: '\x1b[35m',
    cyan: '\x1b[36m'
};

const getTimestamp = () => {
    return new Date().toISOString();
};

class Logger {
    info(message, ...args) {
        console.log(`${colors.cyan}[${getTimestamp()}] INFO:${colors.reset}`, message, ...args);
    }

    error(message, ...args) {
        console.error(`${colors.red}[${getTimestamp()}] ERROR:${colors.reset}`, message, ...args);
    }

    warn(message, ...args) {
        console.warn(`${colors.yellow}[${getTimestamp()}] WARN:${colors.reset}`, message, ...args);
    }

    success(message, ...args) {
        console.log(`${colors.green}[${getTimestamp()}] SUCCESS:${colors.reset}`, message, ...args);
    }

    debug(message, ...args) {
        if (process.env.NODE_ENV === 'development') {
            console.log(`${colors.magenta}[${getTimestamp()}] DEBUG:${colors.reset}`, message, ...args);
        }
    }
}

module.exports = new Logger();